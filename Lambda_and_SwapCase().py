
#SwapCase() Example
s="Hello My Name Is Yash"
#now swapping the Casing of String
print(f"\n\n\n\n = = = = = = = = = = Our First Program is About Swapcase() Function = = = = = = = = = =")
print(f"\n\n\nThis is the Original String = = = = = > {s}\n\n")
print(f"While this is the Swapped One = = = = = > {s.swapcase()}\n\n")

print(f" = = = = = = = = = =This Program Is Ended without any Error  = = = = = = = = = =\n\n")


#Lambda Function Example
